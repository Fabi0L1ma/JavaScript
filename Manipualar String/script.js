var nome = " Fabio Silva de Lima "

console.log(nome.length) //QTD de String
console.log(nome.charAt(2)) //String da Posição
console.log(nome.indexOf("S")) // Indica a posição da String
console.log(nome.replace("Silva", "Miranda")) //Troca de String
console.log(nome.substr(6, 5)) //Pega uma determinada posição da String:
console.log(nome.toLowerCase()) //Coloca tudo em minusculo
console.log(nome.toUpperCase()) //Coloca tudo em maiusculo
console.log(nome.trim()) //Tira espacos das extremidades



